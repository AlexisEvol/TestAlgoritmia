package Ejercicio2;

public class Persona {
    private final int edad;
    private final String sexo;

    public Persona(int edad, String sexo) {
        this.edad = edad;
        this.sexo = sexo;
    }

    public int getEdad() {
        return edad;
    }

    public String getSexo() {
        return sexo;
    }
    
}
